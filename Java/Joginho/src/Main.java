    public void round(JButton bt, int x, int y) {
        bt.setEnabled(false);
        if (ply == 1) {
            mat[x][y] = 1;
            bt.setText("X");
            ply = 2;
            winner = ply1;
            checkRound(1);
        } else {
            mat[x][y] = 2;
            bt.setText("O");
            ply = 1;
            winner = ply2;
            checkRound(2);
        }
        qnt++;
    }
    
    public void checkRound(int x) {
        if (win(x) == true) {
            JOptionPane.showMessageDialog(null, "O jogador " + winner + " venceu!", "Vit√≥ria", JOptionPane.INFORMATION_MESSAGE);
            endGame();
        }
    }
    
    public boolean win(int x) {
        for(int i = 0; i < mat.length; i++) {
            if ( (mat[i][0] == x) && (mat[i][1] == x) && (mat[i][2] == x) )
                return true;
            if ( (mat[0][i] == x) && (mat[1][i] == x) && (mat[2][i] == x) )
                return true;
        }
        if ( (mat[0][0] == x) && (mat[1][1] == x) && (mat[2][2] == x) )
            return true;
        if ( (mat[0][2] == x) && (mat[1][1] == x) && (mat[2][0] == x) )
            return true;
        return false;
    }
    
    public void endGame() {
        for (int i = 0; i < 9; i++)
            bt[i].setEnabled(false);
    }
    
    public void clear() {
        for (int i = 0; i < 9; i++) {
            bt[i].setEnabled(true);
            bt[i].setText("");
        }
        for (int x = 0; x < 3; x++) {
            for (int y = 0; y < 3; y++)
                mat[x][y] = 0;
        }
        ply = 1;
        ply1 = "";
        ply2 = "";
        winner = "";
    }

